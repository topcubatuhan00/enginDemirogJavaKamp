package homework;

public class switchStatement {

	public static void main(String[] args) {

		char condition = 'B';
		
		switch(condition) {
		case 'A':
			System.out.println("A");
			break;
		case 'B':
			System.out.println("B");
			break;
		default:
			System.out.println("No Selected");
			break;
		}

	}

}
